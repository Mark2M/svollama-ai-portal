  <h1>Team Mode (nice to have, maybe...)</h1>
  <p>Pick an AI team and give them a problem to solve, i.e.:</p>
  <ul>
    <li>Identify a open ended problem requiring a solution involving skills from multiple disciplines.</li>
    <li>List the disciplines required.</li>
    <li>Select the models best suited to those disciplines.</li>
    <li>Break down the problem into discipline specific areas.</li>
    <li>Assign each model to a discipline and role, e.g. accountant in finance.</li>
    <li>Assign a model to the role of governor and get it to:
      <ol style="padding-top: .42em;">
        <li>Filter out invalid or off-topic responses.</li>
        <li>Control the sequence in which models can answer.</li>
        <li>Control which models can answer which questions.</li>
      </ol>
    </li>
    <li>Set limits and controls to prevent infinite loops etc.</li>
    <li>Etc.</li>
  </ul>